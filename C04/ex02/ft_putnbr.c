#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	to_ascii;

	to_ascii = '0';
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	to_ascii = (nb % 10) + '0';
	write(1, &to_ascii, 1);
}
